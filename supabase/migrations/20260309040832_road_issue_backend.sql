create extension if not exists pgcrypto;

do $$
begin
	if not exists (select 1 from pg_type where typname = 'user_role') then
		create type public.user_role as enum ('user', 'admin');
	end if;

	if not exists (select 1 from pg_type where typname = 'issue_status') then
		create type public.issue_status as enum ('reported', 'in_review', 'resolved', 'rejected');
	end if;

	if not exists (select 1 from pg_type where typname = 'issue_severity') then
		create type public.issue_severity as enum ('low', 'medium', 'high', 'critical');
	end if;
end
$$;

create table if not exists public.profiles (
	id uuid primary key references auth.users (id) on delete cascade,
	full_name text,
	role public.user_role not null default 'user',
	created_at timestamptz not null default now(),
	updated_at timestamptz not null default now()
);

create table if not exists public.road_issues (
	id uuid primary key default gen_random_uuid(),
	reporter_id uuid not null references public.profiles (id) on delete restrict,
	title text not null check (char_length(title) between 5 and 140),
	description text not null check (char_length(description) between 10 and 2000),
	status public.issue_status not null default 'reported',
	severity public.issue_severity not null default 'medium',
	latitude numeric(9, 6) not null check (latitude between -90 and 90),
	longitude numeric(9, 6) not null check (longitude between -180 and 180),
	address text,
	image_path text not null,
	assigned_to uuid references public.profiles (id) on delete set null,
	admin_notes text,
	resolved_at timestamptz,
	created_at timestamptz not null default now(),
	updated_at timestamptz not null default now()
);

create table if not exists public.issue_updates (
	id bigint generated by default as identity primary key,
	issue_id uuid not null references public.road_issues (id) on delete cascade,
	updated_by uuid not null references public.profiles (id) on delete restrict,
	old_status public.issue_status,
	new_status public.issue_status,
	comment text,
	created_at timestamptz not null default now()
);

create index if not exists idx_road_issues_status on public.road_issues (status);
create index if not exists idx_road_issues_created_at on public.road_issues (created_at desc);
create index if not exists idx_road_issues_severity on public.road_issues (severity);
create index if not exists idx_road_issues_lat_lng on public.road_issues (latitude, longitude);
create index if not exists idx_issue_updates_issue_id on public.issue_updates (issue_id);

create or replace function public.set_updated_at()
returns trigger
language plpgsql
as $$
begin
	new.updated_at = now();
	return new;
end;
$$;

create or replace function public.is_admin()
returns boolean
language sql
stable
security definer
set search_path = public
as $$
	select exists (
		select 1
		from public.profiles p
		where p.id = auth.uid() and p.role = 'admin'
	);
$$;

create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
	insert into public.profiles (id, full_name)
	values (
		new.id,
		coalesce(new.raw_user_meta_data ->> 'full_name', split_part(new.email, '@', 1))
	)
	on conflict (id) do nothing;

	return new;
end;
$$;

create or replace function public.log_issue_status_change()
returns trigger
language plpgsql
as $$
begin
	if new.status is distinct from old.status then
		insert into public.issue_updates (issue_id, updated_by, old_status, new_status, comment)
		values (new.id, auth.uid(), old.status, new.status, new.admin_notes);

		if new.status = 'resolved' and old.status is distinct from 'resolved' then
			new.resolved_at = now();
		end if;
	end if;

	return new;
end;
$$;

