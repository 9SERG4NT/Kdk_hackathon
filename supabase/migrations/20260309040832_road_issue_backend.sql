create or replace view public.issue_public_feed
with (security_invoker = true)
as
select
	ri.id,
	ri.title,
	ri.description,
	ri.status,
	ri.severity,
	ri.latitude,
	ri.longitude,
	ri.address,
	ri.image_path,
	ri.created_at,
	p.full_name as reporter_name
from public.road_issues ri
join public.profiles p on p.id = ri.reporter_id;

create or replace function public.admin_issue_dashboard()
returns table (
	total_issues bigint,
	reported_issues bigint,
	in_review_issues bigint,
	resolved_issues bigint,
	rejected_issues bigint,
	critical_issues bigint,
	issues_last_7_days bigint
)
language plpgsql
security definer
set search_path = public
as $$
begin
	if not public.is_admin() then
		raise exception 'admin privileges required';
	end if;

	return query
	select
		count(*) as total_issues,
		count(*) filter (where status = 'reported') as reported_issues,
		count(*) filter (where status = 'in_review') as in_review_issues,
		count(*) filter (where status = 'resolved') as resolved_issues,
		count(*) filter (where status = 'rejected') as rejected_issues,
		count(*) filter (where severity = 'critical') as critical_issues,
		count(*) filter (where created_at >= now() - interval '7 day') as issues_last_7_days
	from public.road_issues;
end;
$$;

create or replace function public.nearby_open_issues(
	p_latitude double precision,
	p_longitude double precision,
	p_radius_km double precision default 5
)
returns table (
	id uuid,
	title text,
	description text,
	status public.issue_status,
	severity public.issue_severity,
	latitude numeric,
	longitude numeric,
	address text,
	image_path text,
	created_at timestamptz,
	distance_km double precision
)
language sql
stable
as $$
	select
		ri.id,
		ri.title,
		ri.description,
		ri.status,
		ri.severity,
		ri.latitude,
		ri.longitude,
		ri.address,
		ri.image_path,
		ri.created_at,
		(
			6371 * acos(
				least(
					1,
					greatest(
						-1,
						cos(radians(p_latitude))
						* cos(radians(ri.latitude::double precision))
						* cos(radians(ri.longitude::double precision) - radians(p_longitude))
						+ sin(radians(p_latitude)) * sin(radians(ri.latitude::double precision))
					)
				)
			)
		) as distance_km
	from public.road_issues ri
	where ri.status in ('reported', 'in_review')
	and (
		6371 * acos(
			least(
				1,
				greatest(
					-1,
					cos(radians(p_latitude))
					* cos(radians(ri.latitude::double precision))
					* cos(radians(ri.longitude::double precision) - radians(p_longitude))
					+ sin(radians(p_latitude)) * sin(radians(ri.latitude::double precision))
				)
			)
		)
	) <= p_radius_km
	order by distance_km asc;
$$;

